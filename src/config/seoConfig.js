// Helper function to get SEO data for any page/product
export const getSEOData = (type, slug = null) => {
  let seoData = { ...SEO_CONFIG.default };

  switch (type) {
    case 'page':
      if (slug && SEO_CONFIG.pages[slug]) {
        seoData = { ...seoData, ...SEO_CONFIG.pages[slug] };
      }
      break;

    case 'category':
      if (slug && SEO_CONFIG.categories[slug]) {
        seoData = { ...seoData, ...SEO_CONFIG.categories[slug] };
      }
      break;

    case 'product':
      if (slug && SEO_CONFIG.products[slug]) {
        seoData = { ...seoData, ...SEO_CONFIG.products[slug] };
      }
      break;

    default:
      break;
  }

  return seoData;
};

